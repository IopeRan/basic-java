package com.BelajarJava;

public class OperasiPerbandingan {
    public static void main(String[] args) {
        // Operasi Perbandingan Java
        int valueB = 100;
        int valueA = 100;

        System.out.println(valueA > valueB);
        System.out.println(valueA < valueB);
        System.out.println(valueA >= valueB);
        System.out.println(valueA <= valueB);
        System.out.println(valueA == valueB);
        System.out.println(valueA != valueB);
    }
}
